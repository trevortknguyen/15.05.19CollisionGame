package renderEngine;

/**
 * Stores a Color as a three float data structure.
 * @author Thai Nguyen
 *
 */
public class Color {
	public final float r, g, b, a;

	public Color(float r, float g, float b, float a) { //sets color to rgb values
		this.r = r;
		this.g = g;
		this.b = b;
		this.a = a;
	}
	
}
